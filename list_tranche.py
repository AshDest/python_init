#ulisation des tranches avec Des listes

liste = ["user_1",
         "user_2",
         "user_3",
         "user_4"]
print(liste[0:3]) # A partir de l'index 0 de la liste, jusqu' a l'index 3 de la liste
print(liste[-4:-1]) # A partir de l'index -4 jusqu'a l'index -1 de la liste
print(liste[:2]) # A partir du debut, jusqu'a l'index 2 de la liste
print(liste[2:]) # A partir de l'index 2, jusqu'a la fin de la liste
print(liste[::2]) #utilisation des pas dans une liste