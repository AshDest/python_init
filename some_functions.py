# ===  Fonction permettant de voir la Longeur =======
print(len("Python")) # Longueur d'une chaine de caractere
print(len([1,2,3,5,8])) # Longuer d'une liste

# ===  Fonction permettant d'Arrondir une valeur decimal ====
print(round(2.2)) # il va arrondir a 2
print(round(2.7)) # il va arrondir a 3

# === Fonction permettant de voir la valeur maximum et la valeur minimum d'une liste ====

print(min([1,2,4,3])) # Minimum
print(max([1,2,4,3])) # Maximum